using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyFirstConsoleApp
{
    internal class DoWhile
    {
        public static void Main10()
        {
            int j = 1;
            do
            {
                Console.WriteLine(j);
                j++;
            } while (j < 10);
        }
    }
}
